/* ── i18n Engine ─────────────────────────────────────────── */
class I18n {
    constructor() {
        this.currentLang = this.getSavedLang() || 'kk';
        this.init();
    }

    getSavedLang() {
        try {
            return localStorage.getItem('devcore_lang');
        } catch {
            return null;
        }
    }

    saveLang(lang) {
        try {
            localStorage.setItem('devcore_lang', lang);
        } catch {}
    }

    init() {
        this.setLang(this.currentLang, false);
        this.setupLangSwitcher();
    }

    setLang(lang, animate = true) {
        if (!translations[lang]) return;
        this.currentLang = lang;
        this.saveLang(lang);

        // Update HTML lang attribute
        document.documentElement.lang = lang;

        // Update all translatable elements
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            const text = translations[lang][key];
            if (!text && text !== '') return;

            const attrTarget = el.getAttribute('data-i18n-attr');
            if (attrTarget) {
                el.setAttribute(attrTarget, text);
            } else {
                if (animate) {
                    el.style.opacity = '0';
                    el.style.transform = 'translateY(4px)';
                    setTimeout(() => {
                        el.textContent = text;
                        el.style.opacity = '1';
                        el.style.transform = 'translateY(0)';
                    }, 150);
                } else {
                    el.textContent = text;
                }
            }
        });

        // Update meta title
        const titleKey = translations[lang]["meta.title"];
        if (titleKey) document.title = titleKey;

        // Update active lang in switcher
        document.querySelectorAll('.lang-switcher__dropdown li').forEach(li => {
            li.classList.toggle('active', li.getAttribute('data-lang') === lang);
        });

        // Update current lang display
        const flagMap = { kk: 'KZ', ru: 'RU', en: 'EN' };
        const currentFlag = document.querySelector('.lang-switcher__current .lang-switcher__flag');
        if (currentFlag) currentFlag.textContent = flagMap[lang];
    }

    setupLangSwitcher() {
        const btn = document.querySelector('.lang-switcher__current');
        const dropdown = document.querySelector('.lang-switcher__dropdown');
        if (!btn || !dropdown) return;

        btn.addEventListener('click', (e) => {
            e.stopPropagation();
            const isOpen = dropdown.classList.contains('open');
            dropdown.classList.toggle('open');
            btn.setAttribute('aria-expanded', !isOpen);
        });

        dropdown.querySelectorAll('li').forEach(li => {
            li.addEventListener('click', () => {
                const lang = li.getAttribute('data-lang');
                this.setLang(lang, true);
                dropdown.classList.remove('open');
                btn.setAttribute('aria-expanded', 'false');
            });
        });

        document.addEventListener('click', () => {
            dropdown.classList.remove('open');
            btn.setAttribute('aria-expanded', 'false');
        });
    }
}

// Initialize
window.devCoreI18n = new I18n();
